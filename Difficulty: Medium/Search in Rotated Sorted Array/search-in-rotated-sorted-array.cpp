class Solution {
  public:
    int search(vector<int>& arr, int key) {
    set<int> s(arr.begin(), arr.end());
    
    if (s.count(key)) { 
        return distance(arr.begin(), find(arr.begin(), arr.end(), key));
    } else {
        return -1;
    }
}
};

