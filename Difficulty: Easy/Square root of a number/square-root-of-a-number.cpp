// Function to find square root
// x: element to find square root
class Solution {
  public:
    long long int floorSqrt(long long int n) {
        // Your code goes here
        int ans = 1;
        for(int i = 1; i*i <= n; i++){
            if(i*i == n){
                ans = i;
            }
            else{
                ans = i;
            }
        }
        return ans;
    }
};

